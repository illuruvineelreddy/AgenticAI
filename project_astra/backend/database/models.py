class RegimeState(Base):
    """Market regime states."""
    __tablename__ = 'regime_states'
    
    id = Column(Integer, primary_key=True, autoincrement=True)
    regime = Column(String(20), nullable=False)  # BULL, BEAR, SIDEWAYS, HIGH_VOL, EVENT_MODE
    confidence = Column(Float, nullable=False)
    vix_level = Column(Float)
    trend_strength = Column(Float)
    volatility_level = Column(Float)
    breadth = Column(Float)
    allowed_strategies = Column(JSON)
    risk_multiplier = Column(Float, default=1.0)
    timestamp = Column(DateTime, nullable=False, index=True)
    created_at = Column(DateTime, default=datetime.utcnow)
    
    __table_args__ = (
        Index('idx_regime_time', 'timestamp'),
    )


class ModelVersion(Base):
    """ML model versioning and metadata."""
    __tablename__ = 'model_versions'
    
    id = Column(Integer, primary_key=True, autoincrement=True)
    model_name = Column(String(100), nullable=False)
    version = Column(String(50), nullable=False)
    model_type = Column(String(50))  # XGBoost, LightGBM, etc.
    model_path = Column(String(255), nullable=False)
    training_date = Column(DateTime, nullable=False)
    accuracy = Column(Float)
    sharpe = Column(Float)
    max_drawdown = Column(Float)
    features_used = Column(JSON)
    is_active = Column(Boolean, default=False)
    created_at = Column(DateTime, default=datetime.utcnow)
    
    __table_args__ = (
        UniqueConstraint('model_name', 'version', name='uq_model_version'),
    )


